    private void pesquisaAluno1() {
        String sql = "select aluno.idaluno as ID_Aluno, aluno.nome as Nome, universidade.nome as Universidade, curso.nome as Curso,\n"
                + "turma.nome as Turma, aluno.ano_escolar as 'Ano', aluno.ano_lectivo as 'Ano Lectivo', aluno.telefone as Tel, aluno.telefone_alter as Tel2,\n"
                + "aluno.email as Email from aluno, turma, curso, universidade where idaluno like ?";
        try {
            pst = conexao.prepareStatement(sql);

            pst.setString(1, txtPesquisa.getText() + "%");

            rs = pst.executeQuery();

            tabAluno.setModel(DbUtils.resultSetToTableModel(rs));
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e);
        }
    }

